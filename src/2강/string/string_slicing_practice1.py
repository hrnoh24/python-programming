# 문자열 정의
text = "Python Programming"

# 문자열의 일부분 추출하기
print("처음부터 6번째 문자까지:", text[:6])  # 'Python'

# 7번째 문자부터 끝까지
print("7번째 문자부터 끝까지:", text[7:])  # 'Programming'

# 'Pro' 추출하기
print("'Pro' 추출:", text[7:10])

# 전체 문자열 추출하기
print("전체 문자열:", text[:])

# 뒤에서 세 번째 문자까지 추출하기
print("뒤에서 세 번째 문자까지:", text[:-3])